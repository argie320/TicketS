class ItemDetails {

    public $item;
    public $Unit_Brand;
    public $Unit_Model;
    public $Machine_Serial_No;
    public $Part_No_Quantity;
    public $Quantity;
    public $Warranty;
    public $sv_number;

    public function __construct(){


    }

    public function addDetails($item , $Unit_Brand, $Unit_Model, $Machine_Serial_No, $Part_No_Quantity, $Quantity, $Warranty, $sv_number){

        $this->item[] =$item;
        $this->Unit_Brand[] = $Unit_Brand;
        $this->Unit_Model[] = $Unit_Model;
        $this->Machine_Serial_No[] = $Machine_Serial_No;
        $this->Part_No_Quantity[] = $Part_No_Quantity;
        $this->Quantity[] = $Quantity;
        $this->Warranty[] = $Warranty;
        $this->sv_number[] = $sv_number;

    }


}

?>